package DTO;

/**
 *
 * @author chris
 */
public class Item {
    
    private int id;
    private String name;
    private double cost;
    private int stock;

    public Item() {
    }

    public Item(String name, double cost, int id, int stock) {
        this.name = name;
        this.cost = cost;
        this.id = id;
        this.stock = stock;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getCost() {
        return cost;
    }

    public void setCost(double cost) {
        this.cost = cost;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
    
    
    
}
